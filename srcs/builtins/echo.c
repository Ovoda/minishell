# include "minishell.h"

void	echo(char *str, int flag, int fd)
{
	size_t i;

	i = 0;
	while (str[i])
		i++;
	write(fd, str, i);
	if (flag == FALSE)
		write(fd, "\n", 1);
	exit(0);
}
